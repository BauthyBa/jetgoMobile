export default function EmojiPicker({ onEmojiSelect, isOpen, onClose }) {
  const [activeCategory, setActiveCategory] = useState('smileys')
  const [searchQuery, setSearchQuery] = useState('')
  const pickerRef = useRef(null)

  useEffect(() => {
    function handleClickOutside(event) {
      if (pickerRef.current && !pickerRef.current.contains(event.target)) {
        onClose()
      }
    }

    if (isOpen) {
      document.addEventListener('mousedown', handleClickOutside)
      return () => document.removeEventListener('mousedown', handleClickOutside)
    }
  }, [isOpen, onClose])

  if (!isOpen) return null

  const filteredEmojis = searchQuery
    ? Object.values(EMOJI_CATEGORIES).flatMap(cat => 
        cat.emojis.filter(emoji => emoji.includes(searchQuery))
      )
    : EMOJI_CATEGORIES[activeCategory].emojis

  return (
    <div
      ref={pickerRef}
      style={{
        position: 'absolute',
        bottom: '100%',
        right: 0,
        marginBottom: 8,
        width: 320,
        height: 400,
        background: '#1e293b',
        border: '1px solid #334155',
        borderRadius: 12,
        boxShadow: '0 10px 40px rgba(0,0,0,0.3)',
        display: 'flex',
        flexDirection: 'column',
        zIndex: 1000,
      }}
    >
      {/* Barra de búsqueda */}
      <div style={{ padding: 8, borderBottom: '1px solid #334155' }}>
        <input
          type="text"
          placeholder="Buscar"
          value={searchQuery}
          onChange={(e) => setSearchQuery(e.target.value)}
          style={{
            width: '100%',
            padding: '6px 12px',
            background: '#0f172a',
            border: '1px solid #334155',
            borderRadius: 8,
            color: '#e5e7eb',
            fontSize: 14,
            outline: 'none',
          }}
        />
      </div>

      {/* Grid de emojis */}
      <div
        style={{
          flex: 1,
          overflowY: 'auto',
          padding: 8,
          display: 'grid',
          gridTemplateColumns: 'repeat(8, 1fr)',
          gap: 4,
          alignContent: 'start',
        }}
      >
        {filteredEmojis.map((emoji, idx) => (
          <button
            key={`${emoji}-${idx}`}
            onClick={() => {
              onEmojiSelect(emoji)
              setSearchQuery('')
            }}
            style={{
              width: 32,
              height: 32,
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'center',
              fontSize: 24,
              background: 'transparent',
              border: 'none',
              borderRadius: 4,
              cursor: 'pointer',
              transition: 'background 0.2s',
            }}
            onMouseEnter={(e) => e.currentTarget.style.background = '#334155'}
            onMouseLeave={(e) => e.currentTarget.style.background = 'transparent'}
          >
            {emoji}
          </button>
        ))}
      </div>

      {/* Categorías */}
      {!searchQuery && (
        <div
          style={{
            display: 'flex',
            gap: 4,
            padding: 8,
            borderTop: '1px solid #334155',
            overflowX: 'auto',
          }}
        >
          {Object.entries(EMOJI_CATEGORIES).map(([key, category]) => (
            <button
              key={key}
              onClick={() => setActiveCategory(key)}
              style={{
                padding: '6px 8px',
                background: activeCategory === key ? '#334155' : 'transparent',
                border: 'none',
                borderRadius: 6,
                cursor: 'pointer',
                fontSize: 20,
                transition: 'background 0.2s',
                flexShrink: 0,
              }}
              title={category.name}
            >
              {category.emojis[0]}
            </button>
          ))}
        </div>
      )}
    </div>
  )
}


